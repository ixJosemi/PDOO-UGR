    public void setPlayer(Player player){
        
        // se actualiza el atributo de referencia
        playerModel = player;
        
        // se actualiza la vista nombre del jugador
        this.LabelPlayerName.setText(playerModel.getName());
        this.LabelPlayerName.setForeground(Color.red);
        
        // Se actualiza la vista nivel del jugador
        this.LabelLevelPlayer.setText(Integer.toString(playerModel.getLevels()));

        // Se actualiza la vista de nivel de combate del jugador
        this.LabelCombatLevel.setText(Integer.toString(playerModel.getCombatLevel()));
        
        
        // Se actualiza la vista del enemigo del jugador
        this.LabelEnemy.setText(playerModel.getEnemy().getName());
        
        // Se actualiza la vista de si es sectario o no el jugador
        this.LabelCultist.setText(playerModel.IsCultist());
        
        
        // Actualizamos tesoros
        this.fillTreasurePanel(visibleTreasures,playerModel.getVisibleTreasures());
        this.fillTreasurePanel(hiddenTreasures,playerModel.getHiddenTreasures());
        
        repaint();
        revalidate();
    }
     
    private void fillTreasurePanel (JPanel aPanel, ArrayList<Treasure> aList) {
    
        // Se elimina la informaci칩n antigua
        aPanel.removeAll();

        // Se recorre la lista de tesoros construyendo y a침adiendo sus vistas
        // al panel
        for (Treasure t : aList) {
        
            TreasureView aTreasureView = new TreasureView();
            aTreasureView.setTreasure (t);
            aTreasureView.setVisible (true);
            aPanel.add (aTreasureView);
        }

        // Se fuerza la actualizaci칩n visual del panel
        aPanel.repaint();
        aPanel.revalidate();
    }
    
    public void setNapakalaki(Napakalaki model) {

        napakalakiModel = model;
    }
    
    private ArrayList<Treasure> getSelectedTreasures(JPanel aPanel) {
        // Se recorren los tesoros que contiene el panel,
        // almacenando en un vector aquellos que est치n seleccionados.
        // Finalmente se devuelve dicho vector.
        TreasureView tv;
        
        ArrayList<Treasure> output = new ArrayList();
        
        for (Component c : aPanel.getComponents()) {
            
            tv = (TreasureView) c;
            
            if ( tv.isSelected() )
                output.add ( tv.getTreasure() );
            
        }
        
        return output;
    }

